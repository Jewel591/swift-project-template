import SwiftUI

enum BrandSpacing {
    /// 卡片内边距 (22pt)
    static let cardPadding: CGFloat = 22

    /// 卡片内部元素间距 (16pt)
    static let cardContent: CGFloat = 16

    /// Section 之间的间距 (16pt)
    static let section: CGFloat = 16

    /// 根页面水平内边距 (20pt)
    static let pageHorizontal: CGFloat = 20

    /// 紧凑间距 (8pt)
    static let compact: CGFloat = 8
}
